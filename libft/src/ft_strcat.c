char	*ft_strcat(char *s1, const char *s2)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
		++i;
	while ((s1[i] = s2[j]) != '\0')
	{
		++i;
		++j;
	}
	return (s1);
}
