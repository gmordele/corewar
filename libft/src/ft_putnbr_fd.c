#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n < 0)
		write(fd, "-", 1);
	if (n < 10 && n > -10)
	{
		c = n < 0 ? -n + '0' : n + '0';
		write(fd, &c, 1);
	}
	else
	{
		c = n < 0 ? -(n % 10) + '0' : n % 10 + '0';
		n = n < 0 ? -(n / 10) : n / 10;
		ft_putnbr_fd(n, fd);
		write(fd, &c, 1);
	}
}
